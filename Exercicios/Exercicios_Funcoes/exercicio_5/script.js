// Escreva uma função que verifique se uma pessoa é maior de idade ou não e imprima no console.

function verificarIdade(idade) {
    if(idade >= 18) {
        console.log("Você é maior de idade!");

    } else {
        console.log("Você é menor de idade!");

    }
}

verificarIdade(22);
verificarIdade(14);
